    buffer.writeln(_generateNavigation());
    
    // Header section
    buffer.writeln('  <header class="dashboard-header">');
    buffer.writeln('    <div class="container">');
    buffer.writeln('      <h1>üîç Flutter KeyCheck Dashboard</h1>');
