    buffer.writeln('      <section id="trends" class="dashboard-section">');
    buffer.writeln('        <h2>üìä Historical Trends</h2>');
    buffer.writeln(_generateHistoricalTrends(result));
    buffer.writeln('      </section>');
    
    // Issues & Recommendations
